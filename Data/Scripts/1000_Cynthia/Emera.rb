def grantRandomEmera()
    return if !getLooplet
    itemlist = getEmeras() 
    raritylist = getEmeraWeights()
    itemlist.each_with_index do |item, i|
        next if item.length > 0
        raritylist[i] = 0
    end
    raritylist[4] = 0
    totalrarities = raritylist.sum
    if totalrarities == 0  
        pbMessage("No Emeras left to obtain.")
        return
    end
    randomrarity = rand(totalrarities)
    raritylist.each_with_index do |rarity, i|
        if randomrarity < rarity
            randomrarity = i
            break
        else
            randomrarity -= rarity
        end
    end
    randomitem = itemlist[randomrarity][rand(itemlist[randomrarity].length)]
    itemname = EMERADICT[randomitem][:name]
    itemcolor = getEnderChestRarityColors()[randomrarity]
    if getLooplet.pbStoreEmera(randomitem)
        pbMessage("You got \\C[#{itemcolor}]#{itemname}\\C[0]!")
    end
end

def getEmeraWeights()
  return [
    8, #common
    5, #rare
    2, #super rare
    1, #secret rare
  ]
end

def hasEmera?(emera)
    return false if !getLooplet
    return getLooplet.pbHasEmera?(emera)
end

def getEmeras
    emeralist = [
        [ #common
        ],
        [ #uncommon
        ],
        [ #rare
        ],
        [ #legendary
        ],
    ]
    raritylist = [:COMMON, :UNCOMMON, :RARE, :LEGENDARY]
    EMERADICT.each do |emera, values|
        rarity = values[:rarity]
        next if !raritylist.include?(rarity)
        next if getLooplet.emeras.include?(emera)
        emeralist[raritylist.index(rarity)].push(emera)
    end
    return emeralist
end